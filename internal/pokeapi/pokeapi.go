func printLocationPokemon(locationInfo LocationInfo) {
	if len(locationInfo.PokemonEncounters) < 1 {
		fmt.Println("No pokemon found!")
		return
	}
	fmt.Println("Found Pokemon:")
	for _, encounter := range locationInfo.PokemonEncounters {
		fmt.Printf(" - %v\n", encounter.Pokemon.Name)
	}
}

func printLocationArea(locationArea LocationArea) {
	for _, result := range locationArea.Results {
		fmt.Println(result.Name)
	}
}

func checkCache(url string, cache *pokecache.Cache, dest any) {
	if reflect.TypeOf(dest).Kind() != reflect.Ptr {
		log.Fatal("ERROR - checkCache(): dest must be a pointer.")
	}
	data, ok := cache.Get(url)
	if ok {
		err := json.Unmarshal(data, dest)
		if err != nil {
			log.Fatal("ERROR - Unmarshalling json:", err)
		}
	} else {
		response, err := http.Get(url)
		if err != nil {
			log.Fatal(err)
		}
		defer response.Body.Close()

		if response.StatusCode != http.StatusOK {
			log.Fatal("ERROR - Bad response:", response.StatusCode)
		}
		body, err := io.ReadAll(response.Body)
		if err != nil {
			log.Fatal("ERROR - Unable to read response body:", err)
		}
		cache.Add(url, body)
		err = json.Unmarshal(body, &dest)
		if err != nil {
			log.Fatal("ERROR - Unmarshalling json (response):", err)
		}
	}
}

func GetLocationInfo(location string, cache *pokecache.Cache) LocationInfo {
	url := "https://pokeapi.co/api/v2/location-area/" + location
	var locationInfo LocationInfo
	checkCache(url, cache, &locationInfo)
	return locationInfo
}

func getPokemonInfo(name string, cache *pokecache.Cache) PokemonInfo {
	url := "https://pokeapi.co/api/v2/pokemon/" + name
	var pokemonInfo PokemonInfo
	checkCache(url, cache, &pokemonInfo)
	return pokemonInfo
}

func Catch(name string, cache *pokecache.Cache) (Pokemon, bool) {
	fmt.Println("Throwing a Pokeball at " + name + "...")
	pokemonInfo := getPokemonInfo(name, cache)
	if rand.Intn(620) >= pokemonInfo.BaseExperience {
		fmt.Println(name + " was caught!")
		var pokemon Pokemon
		pokemon.Stats = make(map[string]int)
		pokemon.Types = []string{}
		pokemon.Height = pokemonInfo.Height
		pokemon.Weight = pokemonInfo.Weight
		for _, stat := range pokemonInfo.Stats {
			pokemon.Stats[stat.Stat.Name] = stat.BaseStat
		}
		for _, typ := range pokemonInfo.Types {
			pokemon.Types = append(pokemon.Types, typ.Type.Name)
		}
		return pokemon, true
	}
	fmt.Println(name + " escaped!")
	return Pokemon{}, false
}

func PrintPokedexInfo(name string, pokedex map[string]Pokemon) {
	pokemon, caught := pokedex[name]
	if !caught {
		fmt.Println("you have not caught that pokemon")
		return
	}
	fmt.Println("Name: " + name)
	fmt.Printf("Height: %v\n", pokemon.Height)
	fmt.Printf("Weight: %v\n", pokemon.Weight)
	fmt.Println("Stats:")
	fmt.Printf("  -hp: %v\n", pokemon.Stats["hp"])
	fmt.Printf("  -attack: %v\n", pokemon.Stats["attack"])
	fmt.Printf("  -defense: %v\n", pokemon.Stats["defense"])
	fmt.Printf("  -special-attack: %v\n", pokemon.Stats["special-attack"])
	fmt.Printf("  -special-defense: %v\n", pokemon.Stats["special-defense"])
	fmt.Printf("  -speed: %v\n", pokemon.Stats["speed"])
	fmt.Println("Types:")
	for _, typ := range pokemon.Types {
		fmt.Printf("  - %v\n", typ)
	}
}

func Explore(location string, cache *pokecache.Cache) {
	fmt.Printf("Exploring %v...\n", location)
	locationInfo := GetLocationInfo(location, cache)
	printLocationPokemon(locationInfo)
}

func GetMapData(url string, cache *pokecache.Cache) Config {
	var area LocationArea
	config := Config{}
	checkCache(url, cache, &area)

	printLocationArea(area)

	if area.Previous == nil {
		config.Previous = ""
	} else {
		config.Previous = area.Previous.(string)
	}
	if area.Next == nil {
		config.Next = ""
	} else {
		config.Next = area.Next.(string)
	}

	return config
}
